###Operadores de asignacion###
print("..............Operadores de asignacion..............")
a=4; b=5
#asigna una suma 
x=a; x+=b;  print("x+=", x) 
#asigna una resta 
x=a; x-=b;  print("x-=", x)
#asigna una multiplicacion 
x=a; x*=b;  print("x*=", x)
#asigna una division
x=a; x/=b;  print("x/=", x)
#asigna el residuo    
x=a; x%=b;  print("x%=", x)  
#asign el cociente de la divisi√≥n 
x=a; x//=b; print("x//=", x) 
#Calcula una potencia
x=a; x**=b; print("x**=", x) 

